const EmojiPicker = ({ onSelect, onClose }) => {
  const [search, setSearch] = useState('');
  const [activeCategory, setActiveCategory] = useState('Smileys');

  const filtered = useMemo(() => {
    if (!search) return EMOJIS[activeCategory] || [];
    const all = Object.values(EMOJIS).flat();
    return all.filter((e) => e.includes(search));
  }, [search, activeCategory]);

  return (
    <div className="emoji-picker">
      <div className="emoji-search">
        <input
          placeholder="🔍 Search emoji..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          autoFocus
        />
      </div>

      {!search && (
        <div className="emoji-categories">
          {Object.entries(CATEGORIES).map(([icon, name]) => (
            <button
              key={name}
              className={`emoji-cat-btn ${activeCategory === name ? 'active' : ''}`}
              onClick={() => setActiveCategory(name)}
              title={name}
            >
              {icon}
            </button>
          ))}
        </div>
      )}

      <div className="emoji-grid">
        {filtered.map((emoji, i) => (
          <button
            key={i}
            className="emoji-item"
            onClick={() => { onSelect(emoji); }}
          >
            {emoji}
          </button>
        ))}
        {filtered.length === 0 && (
          <div className="emoji-empty">No emojis found</div>
        )}
      </div>
    </div>
  );
};

export default EmojiPicker;